export const streetTypes = [
  '路', '街', '巷', '弄', '大道'
]

export const villageTypes = [
  '村', '庄', '寨', '堡', '疃'
]

export function generateLocation(provinceId = null) {
  const province = provinceId 
    ? provinces.find(p => p.id === provinceId) 
    : provinces[Math.floor(Math.random() * provinces.length)]
  
  const city = province.cities[Math.floor(Math.random() * province.cities.length)]
  const streetType = province.type === '农村' || province.type === '自治区' && Math.random() > 0.5
    ? villageTypes[Math.floor(Math.random() * villageTypes.length)]
    : streetTypes[Math.floor(Math.random() * streetTypes.length)]
  
  const streetNumber = Math.floor(Math.random() * 200) + 1
  const streetName = `${city}${streetType}${streetNumber}号`
  
  return {
    province: province.name,
    provinceId: province.id,
    city: city,
    detail: streetName,
    fullAddress: `${province.name}${streetName}`,
    type: province.type
  }
}

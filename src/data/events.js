// 根据年龄获取事件
export const getAgeEvent = (age) => {
  let stage
  if (age < 3) stage = ageEvents.baby
  else if (age < 7) stage = ageEvents.kindergarten
  else if (age < 13) stage = ageEvents.elementary
  else if (age < 16) stage = ageEvents.middle
  else if (age < 19) stage = ageEvents.high
  else if (age < 23) stage = ageEvents.college
  else if (age < 30) stage = ageEvents.earlyCareer
  else if (age < 40) stage = ageEvents.midCareer
  else if (age < 50) stage = ageEvents.lateCareer
  else if (age < 60) stage = ageEvents.preRetirement
  else if (age < 70) stage = ageEvents.retirement
  else stage = ageEvents.oldAge

  const contents = stage.contents
  const randomContent = contents[Math.floor(Math.random() * contents.length)]
  return { content: randomContent, tags: stage.tags }
}


// 根据天赋获取可能的事件
export const getTalentEvents = (talents) => {
  const events = []
  for (const talent of talents) {
    const talentKey = talent.id || talent
    const talentEventList = talentEvents[talentKey]
    if (talentEventList && Math.random() < 0.3) {
      events.push(talentEventList[Math.floor(Math.random() * talentEventList.length)])
    }
  }
  return events
}
